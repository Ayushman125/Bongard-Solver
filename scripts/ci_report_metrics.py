def read_metric(metric_name: str, metrics_json_path: str) -> Optional[Any]:
    """
    Reads a specific metric from a JSON file generated by benchmark_suite.py.
    Assumes metrics.json contains a structure like:
    {"model_name": {"bongard_accuracy": ..., "relation_map": ...}}
    """
    if not os.path.exists(metrics_json_path):
        logger.warning(f"Metrics JSON file not found at {metrics_json_path}.")
        return None
    try:
        with open(metrics_json_path, 'r') as f:
            metrics_data = json.load(f)
        
        # Assuming benchmark_suite.py outputs a dictionary where keys are model names
        # and values are dictionaries of metrics. We need to find the relevant metric.
        # This assumes we want the metric from the *first* model in the benchmark results.
        if metrics_data:
            first_model_name = list(metrics_data.keys())[0]
            return metrics_data[first_model_name].get(metric_name)
        return None
    except json.JSONDecodeError as e:
        logger.error(f"Error decoding JSON from {metrics_json_path}: {e}")
        return None
    except Exception as e:
        logger.error(f"Error reading metric {metric_name} from {metrics_json_path}: {e}")
        return None

def main(log_dir: str, out_file: str):
    """
    Main function to parse and summarize CI metrics.
    Args:
        log_dir (str): Directory containing HPO study and other logs.
        out_file (str): Path to the output JSON file for summarized metrics.
    """
    logger.info(f"Generating CI report metrics from {log_dir} to {out_file}")
    
    cfg = load_config("config.yaml") # Load a dummy/actual config
    
    hpo_best_value = None
    hpo_study_path = os.path.join(log_dir, cfg['hpo']['output_study_filename'])
    if os.path.exists(hpo_study_path):
        try:
            study = joblib.load(hpo_study_path)
            hpo_best_value = study.best_trial.value
            logger.info(f"HPO Best Trial Value: {hpo_best_value:.4f}")
        except Exception as e:
            logger.error(f"Error loading HPO study from {hpo_study_path}: {e}")
    else:
        logger.warning(f"HPO study file not found at {hpo_study_path}. Skipping HPO best value.")

    # Paths for pruned and quantized models (assuming they are saved in checkpoints dir)
    pruned_model_path = os.path.join(cfg['debug']['save_model_checkpoints'], "optimized_bongard_model.pth") # Assuming pruned model is saved here
    quantized_model_path = os.path.join(cfg['debug']['save_model_checkpoints'], "optimized_bongard_model.pth") # Assuming quantized model overwrites/is saved here

    # Get file sizes
    pruned_size_mb = get_file_size(pruned_model_path)
    # For quant_drop, you'd ideally compare quantized model accuracy to a baseline.
    # Here, we're using a placeholder for the drop calculation.
    quant_accuracy_drop = compute_quant_drop(quantized_model_path) 

    # Read Bongard accuracy from benchmarks.json (created by benchmark_suite.py)
    # Assuming benchmark_suite.py outputs to 'metrics.json' in the current working directory
    # or a specified path. For CI, it's often in the root of the workspace.
    bongard_acc_from_benchmark = read_metric("bongard_accuracy", "metrics.json") # Assuming key is 'bongard_accuracy'

    metrics = {
        "hpo_best_val_accuracy": hpo_best_value,
        "pruned_model_size_mb": pruned_size_mb,
        "quantization_accuracy_drop": quant_accuracy_drop,
        "bongard_solve_accuracy": bongard_acc_from_benchmark
    }

    with open(out_file, "w") as f:
        json.dump(metrics, f, indent=2)
    logger.info(f"CI report metrics written to {out_file}")

